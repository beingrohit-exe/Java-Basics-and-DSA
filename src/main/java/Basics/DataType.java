package Basics;

public class DataType {
    public static void main(String[] args){

        //Primitive Data Type
        int a = 10; // 4 bytes
        float b = 10.25f; // 4 bytes
        byte d = 125; // 1 byte
        short e = 32767; // 2 bytes
        long f = 50; // 8 bytes
        double g = 10.65464; // 8 bytes
        char h = 'A'; // 2 bytes
        boolean i = true; // 1 bit

        //Non-Primitive Data type
        //String, Arrays, Classes, Objects
        String j = "rohit";
    }
}
